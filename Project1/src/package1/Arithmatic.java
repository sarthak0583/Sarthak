package package1;
public class Arithmatic 
{
	public static void add()
	{
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println(c);
	}
	public static void substract()// we can call static method by using method name only
	{
		int a=100;
		int b=200;
		int c=a-b;
		System.out.println(c);
	}
	public static void mulitiple()// we can call static method by using method name only
	{
		int a=100;
		int b=200;
		int c=a*b;
		System.out.println(c);
	}
	public static void divide()// we can call static method by using method name only
	{
		int a=100;
		int b=200;
		int c=b/a;
		System.out.println(c);
	}
	public static void main(String[] args) 
	{
		add();
		substract();
		mulitiple();
		divide();
		
		
	}

}
